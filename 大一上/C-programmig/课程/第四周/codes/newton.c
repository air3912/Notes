/*
==================================================================
题目：牛顿迭代法求a的平方根！迭代公式：Xn+1=(Xn+a/Xn)/2.
==================================================================
*/
#include<stdio.h>
#include<math.h>

int main() {
    double a,x0,x1;
    const double eps = 1e-7;
    
    //cases 
    a = 2;
    
    x0=1;
    x1=x0+1;
    while(fabs(x0-x1) > eps) {
        x0=x1;
        x1=0.5*(x0-a/x0);
    }
    
    printf("%.6lf的平方根是：%.6lf\n",a,x1);
}
